# 指定目录
directory = "./"

# 获取目录下的所有文件名
files = os.listdir(directory)

# 提取文件名中的题目序号
existing_questions = set()
for file in files:
    if '.' in file:
        question_number = file.split('.')[0]
        if question_number.isdigit():
            existing_questions.add(question_number)

# 找出缺失的题目
missing_questions = set(hot100) - existing_questions

# 输出结果
